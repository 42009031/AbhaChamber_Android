package com.abha.chamber.abhachamber_android_app3.common;

public class GenericModel {

    private int image;
    private String name;

    public GenericModel(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
